    <h5>Safety &amp; Self-love</h5>

    <p>A few years ago I recognized a trending theme in some of my coaching sessions one week, the desire to feel safe.  The common denominators: Each client that communicated this desire was a woman, and all of them were referring to relationships with men.</p>

    <p>I believe that one of the most important (if not THE most important) relationship ingredient for a woman is safety.  When I speak of safety I speak of physical safety, of course, and also emotionally safety, and sexual safety. </p>

    <p>As a relationship coach I hear this desire for safety expressed often, and as a woman I can relate to and understand this need.</p>

    <p>One evening I was out riding my bike, and reflecting on my day.</p>

    <p>As I was riding and thinking about the common thread throughout these recent coaching sessions I noticed a beautiful college age woman riding her bicycle around the lakes. She was not wearing a helmet.</p>

    <p>Cyclists without helmets... something I see too often. My thoughts then turned to the drivers I see that are texting while driving and talking on cell phones while driving, once I even saw someone working on a laptop while driving! All unsafe activities that, unfortunately, people engage in all too frequently.</p>

    <p>That is when these two trains of thought collided and merged into another thought; People often live out an identity that is not in alignment with their desire.</p>

    <p>In this example, how can I expect to feel "safe" in a relationship if I am consistently putting out a message to the Universe that safety isn't really one of my primary concerns?</p>

    <p>The world around us is a mirror that reflects exactly what is going on inside of us. Our life experience is always directly related to our identity. </p>

