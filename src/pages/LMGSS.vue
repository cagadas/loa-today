    <h5>Anam Cara</h5>

    <p>One of the most beautiful pictures of soul connection I’ve heard is described in an ancient Celtic vow, called “Anam Cara”, which translates to “soul friendship”.</p>
    <p>"Anam Cara" was a meeting of two souls that was considered destiny, a sweet connection that served to awaken each to the soul's journey.</p>
    <p>The vow itself is incredibly beautiful. In fact, I think it would make a lovely wedding vow!</p>

    <p>◊◊◊</p>
    <p style="text-indent: 0">Anam Cara<br>
    I honor your path.<br>
    I drink from your well.<br>
    I bring an unprotected heart to our meeting-place.<br>
    I hold no cherished outcome.<br>
    I will not negotiate by withholding.<br>
    I am not subject to disappointment.</p>
    <p>◊◊◊</p>

    <p>The idea of a soul relationship that serves to awaken each party to their soul’s journey sounds romantic, and of course it is! However, we must also remember that our soul’s growth often comes in ways that can be uncomfortable as well.</p>
    <p>Your soulmate will be someone who has the privilege of enjoying incredible happiness with you, and also be there with you in times of stress and sorrow. </p>
    <p>All relationships experience growing pains and the stresses of life. Our soulmate relationships sometimes experience these in a deep way, but the stresses of life can also serve to make them stronger and deeper. There is a beautiful Swedish proverb that says, “shared joy is a doubled joy, shared sorrow is half a sorrow.” I think that describes a soulmate relationship in such a lovely way.</p>
    <p>I’ve heard it said that your soulmate is the one that can always “push all of your buttons”.  I’m not sure I would quite put it that way, but I will say that I believe the biggest, brightest mirrors we have are our most intimate relationships, and there is no relationship more intimate than a soulmate relationship.</p>
    <p>Note: Just because soulmate relationships will no doubt experience growing pains at times, this is due to the fact that when we are open to each other in a deep and intimate way we become vulnerable, and when we are vulnerable we can get hurt. The growing pains I’m speaking of have absolutely nothing to do with anything resembling abuse. Physical, emotional, and mental abuse are not factors in any healthy relationship. Your soulmate relationships, both platonic and romantic, will be built on mutual love, trust, and respect. </p>

    <p>Your desire for a deep and intimate soulmate relationship confirms that there are such souls desiring you, too!  It is always true that what you seek is seeking you. </p>
