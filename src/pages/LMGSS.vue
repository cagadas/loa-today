    <h5>An Open Heart</h5>

    <p>Living with an open heart requires vulnerability. And yet we often deny our vulnerability because we don’t want to get hurt. We may develop a hard shell or put an energetic wall up because we are attempting to avoid heartbreak, hurt feelings, or disappointment.</p>

    <p>I want to share a personal story with you because I think it will be helpful, especially if you have closed up because of a bad relationship experience or a broken heart. </p>

    <p>I was involved in an emotionally abusive relationship for more than two decades. I have some pretty serious experience with being hurt &ndash; deeply, and consistently. </p>

    <p>My experience also extends to making myself nearly invulnerable. I put on a hard shell (with a soft smile &ndash; no one needed to actually see that I was putting up a wall of defense). </p>

    <p>I decided that I wouldn’t let myself be hurt, I would be strong, invincible. I would NOT be vulnerable. Looking back, I realize that this seemed to make the pain even worse. The excruciating pain of wanting a healthy relationship so badly, and not seeing any way it would ever happen.</p>

    <p>Eventually, my marriage ended in a nasty divorce, and my own decision to not allow myself to be vulnerable kept me from seeking the support I so desperately needed. </p>

    <p>My best friend didn't even know how bad my marriage had been!</p>

    <p>I didn't want to appear weak, or out of control, so I put on a smile and just pretended everything was fine, even though it wasn't.</p>

    <p>It was painful.  I finally did reach out for support from my wonderful group of friends and family, I did A LOT of personal growth work, I learned how to truly and deeply love myself, and I did finally heal from the pain. It took time. More than 2 years.</p>

    <p>And after I healed is where my story becomes really useful in answering this question I got from a reader: </p>

    <p>"How do I keep the sensitivity and vulnerability of my spirit, without feeling hurt all of the time?"</p>

    <p>As I healed I began to blossom. I opened. I began to really, truly, LOVE my life. I began to really, truly LOVE myself. I felt so alive and so happy. I was living with intention, being authentic, and it felt magical. I had found my path, knew my purpose, and was amazed at how beautiful my life was becoming every single day.</p>

    <p>I felt whole, and because of that I felt I was ready for a relationship. An awesome relationship, of course, and this time I knew what that really meant &ndash; a soulmate relationship full of love, passion, laughter, and mutual respect.</p>

    <p>I met a wonderful man. I fell in love. Everything was completely magical. And then it happened. We had a small disagreement. I cried. We made up. I was okay.  </p>

    <p>The world didn’t end.  </p>

    <p>And then a few weeks later I got hurt again. This time there was NO reasonable or logical excuse for it, it was a total misunderstanding. I KNEW I was "making a mountain out of a molehill" but there it was, I felt wounded. It was a small thing. In fact, it was something that wouldn’t even have bothered most people and yet something got triggered in me and there I was feeling completely torn up about it. Feeling so hurt over something so stupid then had me questioning everything! </p>

    <p>"Why am I suddenly being totally petty?" I was asking myself. This reaction was so unlike anyone I wanted to be. I honestly questioned whether I could do this ‘relationship thing’. (Not a comfortable question to be asking yourself when you are a relationship coach!)</p>

    <p>I was wondering "WHAT IS WRONG WITH ME?!"</p>

    <p>I remember it like it was yesterday. I was standing in my closet, hanging up a sweater and feeling so frustrated about my own emotions, asking myself WHY was I so sensitive? I felt so vulnerable...uh oh.</p>

    <p>Then it hit me. This vulnerability that was allowing me to feel heartache was the very same vulnerability that was allowing me to feel so open, so seen, so heard, so loved, so connected.</p>

    <p>And MY vulnerability and sensitivity were also the very things that allowed my Sweetheart to be open to me, to be honest and real and authentic and vulnerable with me. I remember the epiphany-like realization that I needed to decide now whether it was worth it, because I knew I could not have one without the other.</p>

    <p>Was the risk of a broken heart worth the magic of true love?</p>

    <p>Was feeling hurt over an occasional misunderstanding worth feeling the thrill of deep, pure connection?</p>

    <p>Was I willing to be open to being triggered so that my own soul, my own wounds, could be healed?</p>

    <p>Was this wide open heart that was totally vulnerable and unprotected worth the rapture of having a soulmate relationship?</p>

    <p>My answer was, and is, an unequivocal YES.</p>

    <p>Misunderstandings and miscommunications are inevitable in relationship, especially a brand new relationship. </p>

    <p>These things happen because we don’t yet know each other well, and they are the very things that enable us to know each other better. These experiences are the very things that give us practice in responding in a conscious way, in Being who we want to Be.  </p>

    <p>Our intimate relationships are the clearest mirror we have to show us who we are, and sometimes it feels uncomfortable, sometimes it can hurt.</p>

    <p>But these things are also the place where we both can grow into our full potential...as friends, as lovers, as soulmates, and human beings.</p>

    <p>You do not have to feel hurt all of the time. You can certainly allow yourself to open, to be vulnerable, to be sensitive and to allow those qualities to support you in connecting, in loving yourself and others, and in becoming stronger emotionally.</p>

    <p>If you are in a place where you are feeling hurt all of the time it’s time to figure out why. Basically when we feel hurt (or any other uncomfortable unwanted feeling or emotion) it is because we have a need that is not being met.</p>

    <p>Vulnerability and the kind of growth we experience in a soulmate relationship can be uncomfortable. However, loving relationships contain mutual respect and are not abusive in any way. </p>

    <p>Abusive behavior is never acceptable. Abuse is always the poison apple in a relationship. Your self-love and self-respect will always say "NO" to abuse. </p>

    <p>Staying in an abusive relationship is a very big red flag that you are not loving yourself. You cannot be showing yourself love and allowing yourself to be abused at the same time.</p>

    <p>If you are experiencing pain in your current relationship or in your life it is because there is a need you have that is not being met. Making sure that you are getting your needs met is what self-love is all about. And remember, you don't need a certain person to do a certain thing to meet your needs...your needs are your responsibility. And taking responsibility for your own happiness is the most powerful thing you can do as a conscious creator. </p>

    <p>Pay attention to your feelings. Your feelings are a miracle. All of them. They are there to guide you into the fullest most joyful expression of your unique path and purpose.</p>

