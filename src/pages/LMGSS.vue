    <h5>Trust</h5>

    <p>We've all had experiences that left a bitter taste in our mouth as far as feeling able to trust someone. </p>

    <p>We told someone our deepest secret and they shared it. </p>

    <p>We believed someone when they told us they'd be there for us and then they flaked out. </p>

    <p>We thought we knew someone and then we found out we didn't know them after all. </p>

    <p>These are common life lessons that most of us have learned. It always stings a bit to have our trust betrayed. But here are some things I've learned about trust… </p>

    <p>Trust is built over time. </p>

    <p>Trust begins with you trusting yourself.</p>
    
    <p>When we trust someone and our trust is betrayed we are not the ones who've done anything wrong. We mustn't blame ourselves, and it will do us a lot of good to forgive our self as well as the one who breached the trust. </p>

    <p>That doesn't mean that we are okay with it or that we have to give them a "second chance"...it just means that we don't hold unforgiveness in our hearts towards anyone.</p>

    <p>Forgiveness is for you. It takes the weight off you. You don't need to carry around unforgiveness. It blocks trust, love, and even abundance. It hinders our health. It causes endless stress. </p>

    <p>Remember, there are no mistakes, there is just human experience. Choose to know that you will always be there for yourself. Decide that you will always trust yourself. </p>

    <p>Choose to find a treasure in every experience &ndash; even the painful ones.</p>

    <p>Self-trust goes hand in hand with self-love, and self-love is the most important step to being ready for a soulmate relationship. In fact, until you really love yourself you won’t be able to have a satisfying intimate relationship.</p>

