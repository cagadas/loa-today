    <h5>The Love Affair</h5>

    <p>"Most of us also want a fairy tale, but it never turns out that way does it? Because people are not perfect", she said.</p>

    <p>I listened as my client wondered aloud to me.  I was reminded of a question a reader emailed me that echoed a similar thought, "Does ‘true love’ really exist, or is it just a fairy tale?"</p>

    <p>Ah, the "fairy tale love affair". The one we all want. That perfect, shiny, sparkly, romantic relationship that suddenly makes our whole life easy and perfect, the one where the birds are chirping and the bees are singing and the flowers are blooming and the poisoned appl- WAIT! WHAT?  What poisoned apple?</p>

    <p>OH THAAAT poisoned apple.  Yes, the one in the, uh, fairy tale, ahem.  Along with the wicked witches, evil step sisters, malevolent queens, scary talking trees, flying monkeys and all manner of frightening villains and dangerous, fire-breathing circumstances.</p>

    <p>Those are the little details we seem to be forgetting when we fantasize and wish our own lives and loves were more like a fairy tale.  Within the confines of our imagination, within the covers of an enchanted book, it seems easier to remember and focus on the sparkly, shining deliciousness of dancing at midnight at the fairy ball and being swept off our feet by Prince Perfect.  How easily we forget the poisoned apples and flying monkeys. Life is full of experiences, some of them are uncomfortable. Even when we have found the most wonderful relationship possible, life still happens.</p>

    <p>Soulmates aren’t perfect, they’re real.  And soulmate relationships aren’t perfect, they’re real.  But they are, or they can be shiny, sparkly, and romantic.</p>

    <p>Your Prince, or Princess, (aka your Soulmate) won’t be perfect, but will be perfect for you. And even if the one you are with right now seems anything but perfect for you that can change. In fact, when we change, everything changes. I want to show you a few small changes that you can make that will create a world of difference, whether you are already in a relationship, or still looking for your soulmate.</p>

    <p>And, an important note: even though one of the magical parts of the fairy tale is often the idea of being rescued by Prince Perfect, you really only need rescuing if you are a victim.  </p>

    <p>And I know you don’t want to be a victim, right?</p>

    <p>Right?</p>

    <p>So how can we have the sparkly, shiny, dreamy romantic part?  When will it show up for us?</p>

    <p>When we learn how to love our self first.</p>

    <p>Loving our self is the first Love Affair that has to happen. We have to recognize that we are our own soulmate first. </p>

    <p>This is the single most important thing we can do to change our entire life experience for the better.</p>

    <p>We hear it so often that it’s become a cliché. “Love Yourself.” But so often we don’t know how to do it. </p>

    <p>It starts with intention. Loving intention. And commitment. </p>

    <p>If you can’t make a commitment to fully love and accept yourself it will be impossible for you to make that commitment to someone else. And because of the Law of Attraction, no one will be able to love you unconditionally and accept you as you are and fully commit to you if you can’t do that for yourself. The world can’t give you anything that you aren’t willing to give yourself. It’s just that simple.</p>

    <p>Decide today that you will make a loving intention and a strong commitment to love yourself fully and completely. Accept yourself just as you are. Choose to love every single thing about yourself with true unconditional love. </p>

    <p>You may want to employ a self-love mantra or affirmation, such as “I deeply love and accept myself just as I am.”</p>
    
    <p>Note: Self-love is not the same thing as self-care, so even if you are a pro at having "me time", and taking care of yourself with things like manicures and pedicures and massages and vitamins...are you doing these things with a loving intention? Or because you think you "should"?</p>

    <p>What about when you’re feeling down, or angry, or flustered – do you judge yourself for these feelings? Or do you love yourself through it? </p>

    <p>What about those critical thoughts that pop up? Do you listen to them?</p>

    <p>Those critical thoughts that accuse you of being lazy, or of spending too much money, or of not making enough money, or of being ugly, or fat, or too skinny, or…need I say more? The list of self-critical thoughts  and self-talk can be seemingly endless, especially in a world that encourages us to constantly compare ourselves to everyone else.</p>

    <p>Being critical of yourself or of anyone else is big red flag that your self-love is in need of some strengthening. We tend to criticize in others the hidden parts of ourselves that we consider unlovable.</p>

    <p>For the record, Lovely Magician, you are worthy of love and affection and abundance and joy and vibrant health and a soulmate and anything else you desire. Without changing one thing about yourself, you are worthy. It’s totally natural to want to improve ourselves and our relationships, to expand our horizons, create new things, up-level our life! But none of those things create your worth. You are already worthy of the best life has to offer. You are already worthy of great love.</p>

    <p>You can choose today to love everything you are – even the parts of you that you haven’t loved in the past. Love is an action, and a choice. </p>

    <p>I like to think of the way we bathe and dress a baby, we aren't expecting anything from them, and we do these daily caring tasks with a loving intention. No criticism, just pure love.</p>

    <p>Make a decision now that as often as you think of it, you'll give yourself care with a loving intention. Care for yourself with love, the way you might cuddle a pet, or water a favorite plant, or sing to a baby. </p>

    <p>Get a drink of pure water with an intention of self-love. Take yourself on a date to an art museum or for a walk in the park. Put on a special outfit or some good perfume as a ritual of love and adoration. </p>

    <p>Treat yourself like the princess in the fairytale.</p>

    <p>Mirror work is another very powerful way to work on our self-love. Look into the mirror, into your own eyes, and tell yourself "I love you. There is nothing too good for you." It might feel awkward at first, but soon it will become easier as your self-love grows and becomes stronger. </p>

    <p>This is a powerful way to let your subconscious know that you are worthy of love and of the best life has to offer!</p>

    <p>When we become willing to fall in love with our self, we are ready to begin being more open.</p>

    <p>We must be willing to see each other and ourselves through the magic mirror, the one that reflects our own shortcomings and flaws without judgment and lovingly supports us in bettering them. </p>

    <p>Our intimate relationships are the brightest mirror we have to show us where we are in the realm of self-love. </p>

    <p>Remember, the world cannot give us anything we aren't willing to give our self.</p>

    <p>When we decide to love our self it will be easy to create strong boundaries and stop accepting poisoned apples! </p>

    <p>When we love our self we won't accept invitations to drama or conflict. And we will never tolerate physical, verbal, or emotional abuse.</p>

    <p>There may be some scary talking trees and flying monkeys in our life journey, and we can decide that we will focus on the good, the loving, the eternal. </p>

    <p>The elements along the way that are uncomfortable are there to help us learn love and compassion, to help us recognize the wounds that need healing (if we didn't have wounds we'd never get triggered!), to help us support each other in growing and reaching our full potential. </p>

    <p>Real relationships are not perfect, they are real!</p>

    <p>We don’t need rescuing.</p>

    <p>We can choose to make decisions based on love, instead of fear.</p>

    <p>When we enjoy each moment and experience life fully, knowing that the power of love exists right now, in this present moment, we will experience magic.</p>

    <p>When we live in this way, we will recognize that true love is real, and soulmates exist here in our present reality and not in a fairy tale.</p>

